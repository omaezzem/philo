#include "../include/philo.h"

int atoi_ph(char *str, int nb)
{
    int i;

    i = 0;
	while (str[i] && (str[i] >= '0' && str[i] <= '9'))
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
    return (nb);
}
